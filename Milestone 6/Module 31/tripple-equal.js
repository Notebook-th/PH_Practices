// === first check the type, if type is different then return false. 
// if type is same then check the value
// then if value is same return true otherwise false

console.log(2 === 2); //true
console.log(1 === 2); // false
console.log(true === 2); //false
console.log(false === 2); //false
console.log(null === undefined); //false
console.log(0 === ''); //false
